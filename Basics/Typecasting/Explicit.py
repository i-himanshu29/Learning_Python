# This is Typecasting (Explicit Conversion) in Python


# int() → convert to integer
num1 = int(3.9)  
print(num1)       # 3

# float() → convert to float
num2 = float(5)   
print(num2)       # 5.0

# str() → convert to string
num3 = str(25)    
print(num3)       # "25"
print(type(num3)) # <class 'str'>

# list() → convert to list
s = "hello"
print(list(s))    # ['h', 'e', 'l', 'l', 'o']

# tuple() → convert to tuple
print(tuple(s))   # ('h', 'e', 'l', 'l', 'o')
