x = 10
x += 5
print("+= :" , x)  # x = x + 5 → 15

x -= 3
print("-= :", x) # x = x - 3 → 12

x *= 2
print("*= :", x )   # x = x * 2 → 24

x /= 4   
print("/= :", x) # x = x / 4 → 6.0

x %= 5
print("%= :",x)   # x = x % 5 → 1.0

x **= 3
print("**= :", x)  # x = x ** 3 → 1.0

x //= 2  
print("//= :", x)# x = x // 2 → 0.0
