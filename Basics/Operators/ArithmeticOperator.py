x = 10
y = 3

print("Addition", x + y)  # Addition → 13
print("Subtraction", x - y)  # Subtraction → 7
print("Multiplication", x * y)  # Multiplication → 30
print("Division" , x / y)  # Division → 3.333...
print("Modulus", x % y)  # Modulus (remainder) → 1
print("Exponentiation", x ** y) # Exponentiation → 10^3 = 1000
print("Floor division" , x // y) # Floor division → 3
